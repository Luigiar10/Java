public class Operators {
	public static void main(String[] args){
		// Operators are:
		// (+ , *, /, -, %)
		int num1 = 5, num2 = 4;
		// Operator +
		int resultPlus = num1 + num2;
		System.out.println("Sum: " + resultPlus);
		// Operator *
		int resultMul = num1 * num2;
		System.out.println("Mul: " + resultMul);
		// Operator /
		int resultDiv = num1 / num2;
		System.out.println("Div: " + resultDiv);
		// Operator -
		int resultMin = num1 - num2;
		System.out.println("Minus: " + resultMin);
		// Operator %
		int resultMod = num1 % num2;
		System.out.println("Module: " + resultMod);

		System.out.println("Luigi");
	}
}